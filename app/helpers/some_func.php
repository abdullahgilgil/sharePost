   // guv 
   function guv($str){
      $str = trim($str);
      $str = htmlentities($str);
      $str = htmlspecialchars($str);
      $str = addslashes($str);
      return $str;
   } // guv
////////////////////////////////////////////////////////////////////////////////////////////////////
   // startSessionOnce
   function startSessionOnce(){
      if(!isset($_SESSION)){
         session_start();
      }
   } // startSessionOnce
////////////////////////////////////////////////////////////////////////////////////////////////////
   // is_logged_in
   function is_logged_in(){
      if(isset($_SESSION["loggedID"])){
         return true;
      } else {
         return false;
      }
   }  // is_logged_in 
